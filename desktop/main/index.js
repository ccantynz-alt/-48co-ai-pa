app.whenReady().then(async () => {
  // Check macOS accessibility permission
  await checkAccessibilityPermission()

  // Create tray and overlay
  createTray()
  createOverlayWindow()

  // Register global shortcut: Ctrl+Shift+Space (Cmd+Shift+Space on Mac)
  const shortcut = process.platform === 'darwin' ? 'CommandOrControl+Shift+Space' : 'Ctrl+Shift+Space'

  const registered = globalShortcut.register(shortcut, () => {
    toggleRecording()
  })

  if (!registered) {
    console.error('Failed to register global shortcut:', shortcut)
  }

  // Mouse wheel click listener (global)
  // Electron doesn't have a native global mouse listener, so we use the
  // overlay window + a transparent full-screen input catcher for mouse events.
  // For now, users can configure hotkeys in settings.
  // TODO: Add iohook or similar for true global mouse button detection

  // Auto-update check
  if (app.isPackaged) {
    autoUpdater.checkForUpdatesAndNotify()
  }

  // Set login item
  app.setLoginItemSettings({
    openAtLogin: store.get('launchAtLogin', true),
  })

  // Microphone permission check (macOS)
  if (process.platform === 'darwin') {
    const micStatus = systemPreferences.getMediaAccessStatus('microphone')
    if (micStatus !== 'granted') {
      await systemPreferences.askForMediaAccess('microphone')
    }
  }
})

// Don't quit when all windows are closed (we're a tray app)
app.on('window-all-closed', (e) => {
  e.preventDefault()
})

// Clean up on quit
app.on('will-quit', () => {
  globalShortcut.unregisterAll()
})

// Prevent multiple instances
const gotLock = app.requestSingleInstanceLock()
if (!gotLock) {
  app.quit()
} else {
  app.on('second-instance', () => {
    // If user tries to open another instance, show settings
    createSettingsWindow()
  })
}
